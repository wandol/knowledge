package egovframework.kf.common.vo;

public class LogVO {
	private String download_tm;
	private String user_id;
	private String user_nm;
	private String file_idx;
	private String file_nm;
	private int file_size;
	private String down_page;
	private String user_ip;
	private String user_browser;
	private String user_os;
	public String getDownload_tm() {
		return download_tm;
	}
	public void setDownload_tm(String download_tm) {
		this.download_tm = download_tm;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getUser_nm() {
		return user_nm;
	}
	public void setUser_nm(String user_nm) {
		this.user_nm = user_nm;
	}
	public String getFile_idx() {
		return file_idx;
	}
	public void setFile_idx(String file_idx) {
		this.file_idx = file_idx;
	}
	public String getFile_nm() {
		return file_nm;
	}
	public void setFile_nm(String file_nm) {
		this.file_nm = file_nm;
	}
	public int getFile_size() {
		return file_size;
	}
	public void setFile_size(int file_size) {
		this.file_size = file_size;
	}
	public String getDown_page() {
		return down_page;
	}
	public void setDown_page(String down_page) {
		this.down_page = down_page;
	}
	public String getUser_ip() {
		return user_ip;
	}
	public void setUser_ip(String user_ip) {
		this.user_ip = user_ip;
	}
	public String getUser_browser() {
		return user_browser;
	}
	public void setUser_browser(String user_browser) {
		this.user_browser = user_browser;
	}
	public String getUser_os() {
		return user_os;
	}
	public void setUser_os(String user_os) {
		this.user_os = user_os;
	}
	@Override
	public String toString() {
		return "LogVO [download_tm=" + download_tm + ", user_id=" + user_id + ", user_nm=" + user_nm + ", file_idx="
				+ file_idx + ", file_nm=" + file_nm + ", file_size=" + file_size + ", down_page=" + down_page
				+ ", user_ip=" + user_ip + ", user_browser=" + user_browser + ", user_os=" + user_os + "]";
	}
	
	
}
